<h1><img alt="Saturn V carrying Apollo 11" style="float: right;" src="https://i.ibb.co/pQQccCP/sample.jpg" /> <span style="color:#008080"><span style="font-family:Georgia,serif">Apollo 11</span></span></h1>
<div style="width: 600px;">
    <p style="text-align:justify"><strong>Apollo 11</strong> was the spaceflight that landed the first humans, Americans <a href="http://en.wikipedia.org/wiki/Neil_Armstrong">Neil Armstrong</a> and <a href="http://en.wikipedia.org/wiki/Buzz_Aldrin">Buzz Aldrin</a>, on the Moon on July 20<sup>th</sup>, 1969, at 20:18 UTC. Armstrong became the first to step onto the lunar surface 6 hours later on July 21<sup>st</sup> at 02:56 UTC.</p>

    <p style="text-align:justify">Armstrong spent about <s>three and a half</s> two and a half hours outside the spacecraft, Aldrin slightly less; and together they collected 47.5 pounds (21.5&nbsp;kg) of lunar material for return to Earth. A third member of the mission, <a href="http://en.wikipedia.org/wiki/Michael_Collins_(astronaut)">Michael Collins</a>, piloted the <a href="http://en.wikipedia.org/wiki/Apollo_Command/Service_Module">command</a> spacecraft alone in lunar orbit until Armstrong and Aldrin returned to it for the trip back to Earth.</p>

    <h2><span style="color:#008080"><span style="font-family:Georgia,serif">Broadcasting and <em>quotes</em> <a id="quotes" name="quotes"></a></span></span></h2>

</div>

<p style="text-align:justify">Broadcast on live TV to a world-wide audience, Armstrong stepped onto the lunar surface and described the event as:</p>

<blockquote>
    <p><span style="color:#808080">One small step for [a] man, one giant leap for mankind.&nbsp;<img alt="yes" src="https://cdn.ckeditor.com/4.6.0/full-all/plugins/smiley/images/thumbs_up.png" style="height:23px; width:23px" title="yes" /></span></p>
</blockquote>

<p style="text-align:justify">Apollo 11 effectively ended the <a href="http://en.wikipedia.org/wiki/Space_Race">Space Race</a> and fulfilled a national goal proposed in 1961 by the late U.S. President <a href="http://en.wikipedia.org/wiki/John_F._Kennedy">John F. Kennedy</a> in a speech before the United States Congress:</p>

<blockquote>
    <p><span style="color:#808080">[...] before this decade is out, of landing a man on the Moon and returning him safely to the Earth.</span></p>
</blockquote>

