<p>Launched by a <strong>Saturn V</strong> rocket from <a href="http://en.wikipedia.org/wiki/Kennedy_Space_Center">Kennedy Space Center</a> in Merritt Island, Florida on July 16<sup>th</sup>, Apollo 11 was the fifth manned mission of <a href="http://en.wikipedia.org/wiki/NASA">NASA</a>&#39;s Apollo program. The Apollo spacecraft had three parts:</p>

<ol start="1" style="list-style-type:lower-roman">
    <li><strong>Command Module</strong> with a cabin for the three astronauts which was the only part which landed back on Earth.</li>
    <li><strong>Service Module</strong> which supported the Command Module with propulsion, electrical power, oxygen and water.</li>
    <li><strong>Lunar Module</strong> for landing on the Moon.</li>
</ol>

<p style="text-align:justify">After being sent to the Moon by the Saturn V&#39;s upper stage, the astronauts separated the spacecraft from it and travelled for three days until they entered into lunar orbit. Armstrong and Aldrin then moved into the Lunar Module and landed in the <a href="http://en.wikipedia.org/wiki/Mare_Tranquillitatis">Sea of Tranquility</a>. They stayed a total of about 21 and a half hours on the lunar surface. After lifting off in the upper part of the Lunar Module and rejoining Collins in the Command Module, they returned to Earth and landed in the <a href="http://en.wikipedia.org/wiki/Pacific_Ocean">Pacific Ocean</a> on July 24<sup>th</sup>.</p>

